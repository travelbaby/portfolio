const defaultLanguage = "ja";
const storageKey = "portfolio-language";

const getSavedLanguage = () => {
    try {
        const savedLanguage = localStorage.getItem(storageKey);
        return translations[savedLanguage] ? savedLanguage : defaultLanguage;
    } catch (error) {
        return defaultLanguage;
    }
};

const saveLanguage = (lang) => {
    try {
        localStorage.setItem(storageKey, lang);
    } catch (error) {
        // 保存できない環境でも、表示切り替え自体は動かします。
    }
};

const getTranslation = (lang, key) => {
    const value = translations[lang]?.[key];
    if (typeof value === "undefined") {
        console.warn(`[i18n] Missing translation: ${lang}.${key}`);
        return null;
    }
    return value;
};

const setMetaContent = (selector, value) => {
    const element = document.querySelector(selector);
    if (element && value) {
        element.setAttribute("content", value);
    }
};

const applyMeta = (lang) => {
    const meta = translations[lang].meta;
    document.documentElement.lang = lang;
    document.title = meta.title;
    setMetaContent('meta[name="description"]', meta.description);
    setMetaContent('meta[property="og:title"]', meta.ogTitle);
    setMetaContent('meta[property="og:description"]', meta.ogDescription);
    setMetaContent('meta[property="og:site_name"]', meta.siteName);
    setMetaContent('meta[name="twitter:title"]', meta.twitterTitle);
};

const applyText = (lang) => {
    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;
        const value = getTranslation(lang, key);
        if (value !== null) {
            element.innerHTML = value;
        }
    });
};

const applyAttributes = (lang) => {
    document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
        const attrPairs = element.dataset.i18nAttr.split(",");
        attrPairs.forEach((pair) => {
            const [attr, key] = pair.split(":").map((item) => item.trim());
            if (!attr || !key) return;

            const value = getTranslation(lang, key);
            if (value !== null) {
                element.setAttribute(attr, value);
            }
        });
    });
};

const updateLanguageButtons = (lang) => {
    document.querySelectorAll("[data-lang]").forEach((button) => {
        const isActive = button.dataset.lang === lang;
        button.classList.toggle("is-active", isActive);
        button.setAttribute("aria-pressed", String(isActive));
    });
};

const applyLanguage = (lang) => {
    if (!translations[lang]) return;
    applyMeta(lang);
    applyText(lang);
    applyAttributes(lang);
    updateLanguageButtons(lang);
    saveLanguage(lang);
};

document.addEventListener("DOMContentLoaded", () => {
    document.querySelectorAll("[data-lang]").forEach((button) => {
        button.addEventListener("click", () => {
            applyLanguage(button.dataset.lang);
        });
    });

    applyLanguage(getSavedLanguage());
});
